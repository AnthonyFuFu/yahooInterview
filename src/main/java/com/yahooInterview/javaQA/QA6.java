public class QA6 {
//	基本上我熟悉且常用的設計模式就是門面模式(Facade Pattern)、單例模式(Singleton Pattern)、工廠模式(Factory Pattern)
//	門面模式：是讓使用這些程式和系統的人，有一個清楚的介面來使用，算是整理型的模式，讓物件或方法可以統一使用接口去使用。
//	單例模式：讓使用者取得該類別的一個實例，並且無論何時何處取得，得到的都會是同一個實例。
//	工廠模式：將創建的物件實體程式碼交由子類別各自實現，並且會依照物件子類別實體去執行實際實現的方法，實際產生產品的是實作工廠概念的實體工廠。
}
